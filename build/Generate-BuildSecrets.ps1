<#
.SYNOPSIS
    ビルド時にClientIdとClientSecretをXORエンコードしてC#ソースファイルを生成するスクリプト。
    生成されたファイルはgitには含まれず、ビルド時にのみ作成される。
.PARAMETER ClientId
    TwitchのClientId
.PARAMETER ClientSecret
    TwitchのClientSecret
.PARAMETER OutPath
    出力先ディレクトリ（通常はIntermediateOutputPath）
#>
param(
    [string]$ClientId = "",
    [string]$ClientSecret = "",
    [string]$OutPath = "."
)

# XORキー（静的固定値）
$key = [byte[]]@(
    0x4F, 0xA2, 0x31, 0xBE, 0x7C, 0xD5, 0x19, 0xF3,
    0x8A, 0x46, 0xC7, 0x2E, 0x95, 0x6B, 0xD8, 0x14,
    0xE1, 0x53, 0x9F, 0x27, 0xBA, 0x60, 0xFC, 0x3D,
    0x72, 0xAE, 0x85, 0xC1, 0x4B, 0x98, 0x36, 0xEF
)

function ConvertTo-XorBytes([string]$text) {
    $bytes = [System.Text.Encoding]::UTF8.GetBytes($text)
    $result = [byte[]]::new($bytes.Length)
    for ($i = 0; $i -lt $bytes.Length; $i++) {
        $result[$i] = $bytes[$i] -bxor $key[$i % $key.Length]
    }
    return $result
}

function Format-ByteArrayLiteral([byte[]]$bytes) {
    if ($null -eq $bytes -or $bytes.Length -eq 0) {
        return "System.Array.Empty<byte>()"
    }
    $hex = $bytes | ForEach-Object { "0x{0:X2}" -f $_ }
    return "new byte[] { " + ($hex -join ", ") + " }"
}

# XORエンコード
$clientIdBytes  = ConvertTo-XorBytes $ClientId
$clientSecBytes = ConvertTo-XorBytes $ClientSecret

# C# リテラル生成
$clientIdLiteral  = Format-ByteArrayLiteral $clientIdBytes
$clientSecLiteral = Format-ByteArrayLiteral $clientSecBytes

# XORキーのC#リテラル
$keyLiteral = Format-ByteArrayLiteral $key

# 出力ディレクトリを作成
# 末尾クォートや余分な文字を防御的にトリム
$OutPath = $OutPath.TrimEnd('"').TrimEnd("'")
if (-not (Test-Path $OutPath)) {
    New-Item -ItemType Directory -Path $OutPath -Force | Out-Null
}

$outFile = Join-Path $OutPath "BuildSecrets.g.cs"

$content = @"
// <auto-generated/>
// このファイルはビルド時に自動生成されます。手動で編集しないでください。
using System;
using System.Text;

namespace TwitchChatOverlay
{
    internal static class BuildSecrets
    {
        private static readonly byte[] s_key = $keyLiteral;

        private static string Dec(byte[] data)
        {
            var result = new byte[data.Length];
            for (int i = 0; i < data.Length; i++)
                result[i] = (byte)(data[i] ^ s_key[i % s_key.Length]);
            return Encoding.UTF8.GetString(result);
        }

        internal static string ClientId => Dec($clientIdLiteral);

        internal static string ClientSecret => Dec($clientSecLiteral);
    }
}
"@

[System.IO.File]::WriteAllText($outFile, $content, [System.Text.Encoding]::UTF8)
Write-Host "Generated: $outFile"
